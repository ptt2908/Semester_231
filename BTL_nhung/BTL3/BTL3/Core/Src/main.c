void Sine(float A, float f)
{
	//f_pulse = f_timTRIGO/ns (ns la so mau)   khi ARR = 0 thi f_pulse = 10k
	__HAL_TIM_SET_AUTORELOAD(&htim2, (10000/f)-1);
	for(int i = 0; i < 100; i++)
	{
		sine_val[i] = ((sin(i*2*PI/100) + 1)*(float)(4095*A/3));
	}
}

			/*--------------------------------------------------------------------------*/
void Square(float A, float f)
{
	__HAL_TIM_SET_AUTORELOAD(&htim2, (10000/f)-1);

	for(int i = 0; i < 100; i++)
	{
		square_val[i] = (i<50) ? 0: 2.0*A*4095/3;
	}
}
			/*--------------------------------------------------------------------------*/

void triangular(float A, float f)
{
	__HAL_TIM_SET_AUTORELOAD(&htim2, (10000/f)-1);
	
	for(int i = 0; i < 100; i++)
	{
		if(i<50)
			triangular_val[i] = 4095*2*A/3*i/49;
		else
			triangular_val[i] = 4095*2*A/3 - (i-49)*4095*2*A/3/50;
	}
}
